    public void preencherTabela(){
       
       ArrayList dados = new ArrayList(); 
       
       String [] Colunas = new String[]{"Código", "Grupo" ,  "SubGrupo"};
       
       connSubGrupo.conexao();
       connSubGrupo.executaaSQL("SELECT * FROM SUBGRUPO_PRODUTO S JOIN GRUPO_PRODUTO C ON S.CODIGO_GRUPO = C.CODIGO_GRUPO ORDER BY CODIGO_SUBGRUPO;");
       
       try{
          connSubGrupo.rs.first();
          
          do{
             dados.add(new Object[]{connSubGrupo.rs.getInt("CODIGO_SUBGRUPO"), 
                 connSubGrupo.rs.getString("NOME_GRUPO"), connSubGrupo.rs.getString("NOME_SUBGRUPO") });
          }while(connSubGrupo.rs.next()); 
       } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Erro ao preencher Tabela:\n" + e);
         }
          
       
       ModeloTabela modelo = new ModeloTabela(dados, Colunas);
       
       jtSubGrupo.setModel(modelo);
       jtSubGrupo.getColumnModel().getColumn(0).setPreferredWidth(50);
       jtSubGrupo.getColumnModel().getColumn(0).setResizable(true);
       
       jtSubGrupo.getColumnModel().getColumn(1).setPreferredWidth(50);
       jtSubGrupo.getColumnModel().getColumn(1).setResizable(true);
       
       jtSubGrupo.getColumnModel().getColumn(2).setPreferredWidth(50);
       jtSubGrupo.getColumnModel().getColumn(2).setResizable(true);
       
       jtSubGrupo.getTableHeader().setReorderingAllowed(true);
       jtSubGrupo.setAutoResizeMode(jtSubGrupo.AUTO_RESIZE_ALL_COLUMNS);//tamanho automatico conforme como é o layout
      
       jtSubGrupo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    
