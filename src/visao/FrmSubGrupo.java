    private void jbInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInserirActionPerformed
       if(jcbGrupo.getSelectedItem().equals("") || jfSubGrupo.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(null, "Preencha todos os campos");
       } else{
            
            try{
            
               connSubGrupo.conexao();
               connGrupo.conexao();
               connGrupo.executaaSQL("SELECT * FROM GRUPO_PRODUTO WHERE NOME_GRUPO = '" + jcbGrupo.getSelectedItem() + "'");
               connGrupo.rs.first();
               
               modSubGrupo.getGrupo().setCodigo(connGrupo.rs.getInt("CODIGO_GRUPO"));
               modSubGrupo.setNomeSubGrupo(jfSubGrupo.getText());
               
               controle.Inserir(modSubGrupo);
               
               preencherTabela();
               limparCampos();
            } catch(SQLException ex){
                 JOptionPane.showMessageDialog(null, "Erro ao setar e gravar registro:\n" + ex);
              }
            
            
            
         }

    }//GEN-LAST:event_jbInserirActionPerformed

    
    
